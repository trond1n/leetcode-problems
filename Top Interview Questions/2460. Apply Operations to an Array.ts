// 2460. Apply Operations to an Array
// Вам дан массив nums размера n, состоящий из неотрицательных целых чисел. 
// Вам нужно применить n - 1 операций к этому массиву. В i-й операции (с нулевой индексацией) выполните следующее:
// Если nums[i] равно nums[i + 1], умножьте nums[i] на 2 и установите nums[i + 1] в 0.
// Если они не равны, пропустите операцию.
// После выполнения всех операций переместите все нули в конец массива, сохраняя порядок остальных элементов.


function applyOperations(nums: number[]): number[] {
   // Проходим по массиву, кроме последнего элемента
   for (let i = 0; i < nums.length - 1; i++) {
    // Если текущий элемент равен следующему
    if (nums[i] === nums[i + 1]) {
        nums[i] *= 2; // Удваиваем текущий элемент
        nums[i + 1] = 0; // Заменяем следующий элемент на 0
    }
    // Если элементы не равны, просто продолжаем цикл
}

// Используем reduce для создания нового массива без нулей
const result = nums.reduce((acc: number[], curr: number) => {
    if (curr !== 0) {
        acc.push(curr); // Добавляем ненулевые элементы в аккумулятор
    }
    return acc; // Возвращаем аккумулятор для следующей итерации
}, []); // Начальное значение аккумулятора — пустой массив

// Добавляем нули в конец результата
for (let i = 0; i < nums.length; i++) {
    if (nums[i] === 0) {
        result.push(0); // Добавляем нули в конец массива result
    }
}

// Возвращаем итоговый массив
return result;
};